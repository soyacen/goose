// newMessage creates a new zero-value instance of a proto.Message type.
// It handles both pointer type parameters (e.g. *pb.Msg) and value type
// parameters (e.g. pb.Msg) via reflection.
func newMessage[T proto.Message]() T {
	var zero T
	rt := reflect.TypeOf(zero)
	if rt.Kind() == reflect.Pointer {
		return reflect.New(rt.Elem()).Interface().(T)
	}
	return reflect.New(rt).Interface().(T)
}

// GenericClientStream implements the ServerStreamingClient, ClientStreamingClient,
// and BidiStreamingClient interfaces. It is used in generated code.
type GenericClientStream[Req proto.Message, Res proto.Message] struct {
	ClientStream
}

// NewGenericClientStream creates a new GenericClientStream wrapping the given
// ClientStream.
func NewGenericClientStream[Req proto.Message, Res proto.Message](cs ClientStream) *GenericClientStream[Req, Res] {
	return &GenericClientStream[Req, Res]{ClientStream: cs}
}

var (
	_ ServerStreamingClient[*anypb.Any]             = (*GenericClientStream[*anypb.Any, *anypb.Any])(nil)
	_ ClientStreamingClient[*anypb.Any, *anypb.Any] = (*GenericClientStream[*anypb.Any, *anypb.Any])(nil)
	_ BidiStreamingClient[*anypb.Any, *anypb.Any]   = (*GenericClientStream[*anypb.Any, *anypb.Any])(nil)
)

// Send pushes one message into the stream of requests to be consumed by the
// server. The type of message which can be sent is determined by the Req type
// parameter of the GenericClientStream receiver.
func (x *GenericClientStream[Req, Res]) Send(m Req) error {
	return x.ClientStream.SendMsg(m)
}

// Recv reads one message from the stream of responses generated by the server.
// The type of the message returned is determined by the Res type parameter
// of the GenericClientStream receiver.
func (x *GenericClientStream[Req, Res]) Recv() (Res, error) {
	m := newMessage[Res]()
	if err := x.ClientStream.RecvMsg(m); err != nil {
		var zero Res
		return zero, err
	}
	return m, nil
}

// CloseAndRecv closes the sending side of the stream, then receives the unary
// response from the server. The type of message which it returns is determined
// by the Res type parameter of the GenericClientStream receiver.
func (x *GenericClientStream[Req, Res]) CloseAndRecv() (Res, error) {
	if err := x.ClientStream.CloseSend(); err != nil {
		var zero Res
		return zero, err
	}
	m := newMessage[Res]()
	if err := x.ClientStream.RecvMsg(m); err != nil {
		var zero Res
		return zero, err
	}
	return m, nil
}

