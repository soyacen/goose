// SendAndClose pushes the unary response to the client and closes the send
// direction of the stream. The type of message which can be sent is determined
// by the Res type parameter of the clientStreamServer receiver.
func (x *GenericServerStream[Req, Res]) SendAndClose(m Res) error {
	if err := x.ServerStream.SendMsg(m); err != nil {
		return err
	}
	return x.ServerStream.CloseSend()
}

